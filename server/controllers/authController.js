import User from "../models/User.js";
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";

export const registerUser = async (req, res) => {
  try {
    const { name, email, password, role,tienda } = req.body; // üëà agregamos role
    const userExistente = await User.findOne({ email });
    if (userExistente) return res.status(400).json({ message: "El usuario ya existe" });

    const hashed = await bcrypt.hash(password, 10);
    const nuevoUsuario = new User({
      name,
      email,
      password: hashed,
      role: role || "cliente", // üëà guarda el rol enviado o cliente por defecto
      tienda: tienda || null,
    });

    await nuevoUsuario.save();
    res.status(201).json({ message: "Usuario registrado exitosamente" });
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: "Error al registrar usuario" });
  }
};
export const loginUser = async (req, res) => {
  try {
    const { email, password } = req.body;
    const user = await User.findOne({ email }); // ‚úÖ cambiamos a user

    if (!user) {
      return res.status(400).json({ message: "Usuario no encontrado" });
    }

    const isMatch = await bcrypt.compare(password, user.password);
    if (!isMatch) {
      return res.status(400).json({ message: "Contrase√±a incorrecta" });
    }

    // ‚úÖ incluir la tienda y rol en el token
    const token = jwt.sign(
      {
        id: user._id,
        role: user.role,
        tienda: user.tienda,
      },
      process.env.JWT_SECRET,
      { expiresIn: "7d" }
    );

    // ‚úÖ devolver token + datos del usuario
    res.json({
      token,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
        tienda: user.tienda,
      },
    });
  } catch (error) {
    console.error("‚ùå Error al iniciar sesi√≥n:", error);
    res.status(500).json({ message: "Error al iniciar sesi√≥n" });
  }
};
